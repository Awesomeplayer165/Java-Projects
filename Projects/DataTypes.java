class DataTypes {
    public static void main(String[] args) {
        System.out.println("Hello, World!");

        boolean a = true;
        boolean b = false;

        System.out.println(a + " " + b);

        char letter = 'a';
        char letter2 = 97;

        System.out.println(letter + " " + letter2);

        float number = 2.2f;

        double var1 = (float) 44.83;
        double var2 = 743.33216323;

        System.out.println(var1 + " " + var2);
    }
}